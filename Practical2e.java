class Practical2e{
    public static void main(String args[]){
        int m=20;
        int n=30;

        System.out.println("Initial value of m "+m);


        m++;
        System.out.println("Increment: "+m);

        ++m;
        System.out.println("Pre-increment"+m);

        System.out.println("/n Initial value for n"+n);

        n--;
        System.out.println("Post- decrement"+n);

        --n;
        System.out.println("Pre- decrement"+n);

    }
}